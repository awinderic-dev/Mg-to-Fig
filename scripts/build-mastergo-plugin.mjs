import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const outDir = path.join(root, "dist", "mastergo-plugin");
const outSrcDir = path.join(outDir, "src");

function stripSchemaModule(source) {
  return source
    .replace(/export const /g, "const ")
    .replace(/export function /g, "function ");
}

function stripExporterModule(source) {
  return source
    .replace(/import \{[\s\S]*?\} from "\.\.\/\.\.\/\.\.\/packages\/schema\/src\/index\.js";\n\n/, "")
    .replace(/export function /g, "function ");
}

function stripMainModule(source) {
  return source.replace(/import \{ exportFromMasterGo \} from "\.\/exporter\.js";\n\n/, "");
}

const [schema, exporter, main, ui, manifest] = await Promise.all([
  readFile(path.join(root, "packages", "schema", "src", "index.js"), "utf8"),
  readFile(path.join(root, "plugins", "mastergo", "src", "exporter.js"), "utf8"),
  readFile(path.join(root, "plugins", "mastergo", "src", "main.js"), "utf8"),
  readFile(path.join(root, "plugins", "mastergo", "src", "ui.html"), "utf8"),
  readFile(path.join(root, "plugins", "mastergo", "manifest.json"), "utf8")
]);

const bundledMain = [
  "/* Bundled MasterGo exporter plugin. Generated by scripts/build-mastergo-plugin.mjs. */",
  stripSchemaModule(schema),
  stripExporterModule(exporter),
  stripMainModule(main)
].join("\n\n");

await mkdir(outSrcDir, { recursive: true });
await writeFile(path.join(outDir, "manifest.json"), manifest);
await writeFile(path.join(outSrcDir, "main.js"), bundledMain);
await writeFile(path.join(outSrcDir, "ui.html"), ui);
await writeFile(path.join(outSrcDir, "exporter.js"), "// Bundled into src/main.js for MasterGo local plugin runtime.\n");
await writeFile(path.join(outSrcDir, "schema.js"), "// Bundled into src/main.js for MasterGo local plugin runtime.\n");
