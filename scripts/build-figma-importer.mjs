import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const outDir = path.join(root, "dist", "figma-importer-plugin");

function stripSchemaModule(source) {
  return source
    .replace(/export const /g, "const ")
    .replace(/export function /g, "function ");
}

function stripImporterModule(source) {
  return source
    .replace(/import \{[\s\S]*?\} from "\.\.\/\.\.\/\.\.\/packages\/schema\/src\/index\.js";\n\n/, "")
    .replace(/export async function /g, "async function ")
    .replace(/export function /g, "function ");
}

function stripPostprocessModule(source) {
  return source
    .replace(/export function /g, "function ");
}

function stripMainModule(source) {
  return source
    .replace(/import \{ createImportSession, importToFigma \} from "\.\/importer\.js";\n/, "")
    .replace(/import \{ postprocessMasterGoNodes \} from "\.\/postprocess\.js";\n\n/, "");
}

const [schema, importer, postprocess, main, ui, manifest] = await Promise.all([
  readFile(path.join(root, "packages", "schema", "src", "index.js"), "utf8"),
  readFile(path.join(root, "plugins", "figma", "src", "importer.js"), "utf8"),
  readFile(path.join(root, "plugins", "figma", "src", "postprocess.js"), "utf8"),
  readFile(path.join(root, "plugins", "figma", "src", "main.js"), "utf8"),
  readFile(path.join(root, "plugins", "figma", "src", "ui.html"), "utf8"),
  readFile(path.join(root, "plugins", "figma", "manifest.json"), "utf8")
]);

const bundledMain = [
  "/* Bundled Figma importer plugin. Generated by scripts/build-figma-importer.mjs. */",
  stripSchemaModule(schema),
  stripImporterModule(importer),
  stripPostprocessModule(postprocess),
  stripMainModule(main)
].join("\n\n");

await mkdir(outDir, { recursive: true });
await writeFile(path.join(outDir, "main.js"), bundledMain);
await writeFile(path.join(outDir, "ui.html"), ui);
await writeFile(path.join(outDir, "manifest.json"), JSON.stringify({
  ...JSON.parse(manifest),
  main: "main.js",
  ui: "ui.html"
}, null, 2));
